// The os module provides operating system-related utility methods and properties.
const myOs = require('os'); 
// importing the module here & storing in 'os' (can give any name)

console.log(myOs.freemem()); // displays free memory space
console.log(myOs.homedir()); // displays home directory
console.log(myOs.hostname()); // displays host name
console.log(myOs.release()); // the operating system build
console.log(myOs.platform()); // a string identifying the operating system platform.






